#include <iostream>
#include <math.h>

int main()
{
	double a;
	double b;
	double c;
	std::cout << "Will use the formula a^2 + b^2 = c^2 to solve for the hypotenuse of a triangle." << "\n";
	std::cout << "Input a value for a: ";
	std::cin >> a;
	std::cout << "Input a value for b: ";
	std::cin >> b;
	c = sqrt(((a*a)+(b*b)));
	std::cout << "The hypotenuse is: ";
	std::cout << c << "\n";
return(0);
}
	
